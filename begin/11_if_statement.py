# normal if statement
"""
if condition:
    statement_a
else:
    statement_b
"""

# nested condition if statement
"""
if condition_1:
    if condition_2:
        statement_a
    else:
        statement_b
"""

# multi-conditions if statement
"""
if condition_1:
    statement_a
elif condition_2:
    statement_b
elif condition_3:
    statement_c
else:
    statement_d
"""

# logical operation
"""
priority level
not > and > or
"""

color = "red"
if color == "green":
    print("color is green!")
elif color == "red":
    print("color is red!")
else:
    print("color isn't green!")
